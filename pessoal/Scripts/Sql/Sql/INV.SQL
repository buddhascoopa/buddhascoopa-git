explain plan set statement_id = 'INV' into plan_table for 
SELECT POL.QTD_DIA_POLITI_MAX,
       POL.QTD_DIA_POLITI_MINIMA 
FROM   MTL_SYSTEM_ITEMS INV,
       PRODUT_GENERC PRODUT,
  --     POLITI_ESTOQ_MATPRI PEM,
       POLITI_ESTOQ_CONJ POL
WHERE  INV.ORGANIZATION_ID      = POL.IDT_ORGANZ  
  AND  INV.SEGMENT1             = TO_CHAR(PRODUT.COD_PRODUT_GENERC) 
  AND  PRODUT.COD_GRUPO_PRODUT  = POL.COD_GRUPO_PRODUT
  AND  PRODUT.COD_CLASSE_PRODUT = POL.COD_CLASSE_PRODUT
  AND  POL.DSC_MERCAD_COMPRA IS NULL  
  AND  POL.DSC_PROCDA        IS NULL
  AND  POL.DSC_TIPO_ITEM     IS NULL
  AND  INV.INVENTORY_ITEM_ID =  1081926
  AND  INV.ORGANIZATION_ID   =  1 
/
